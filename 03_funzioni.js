// Creazione ed utilizzo Funzioni

// Creare una funzione raddoppia
// per calcolare il doppio del valore passato
function raddoppia(x) {
     return x * 2;
}

function somma(a, b) {
    return a + b;
}

// Utilizzare funzione raddoppia memorizzando il valore
// doppio in una variabile locale da stampare a console
var result = raddoppia(3);
console.log(result);

result = somma(7, 21);
console.log(result);
